#include "cub3d.h"

void	free_double_array(char **map, int cnt)
{
	int i;

	i = 0;
	if (map[i])
	{
		while (i < cnt)
		{
			free(map[i]);
			i++;
		}
	}
	free(map);
	map = 0;
}
